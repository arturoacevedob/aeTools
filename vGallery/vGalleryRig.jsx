    function findEffectByName(layer, name) {
        var ep;
        try { ep = layer.property("ADBE Effect Parade"); } catch (e) { return null; }
        if (!ep) { return null; }
        for (var i = 1; i <= ep.numProperties; i++) {
            if (ep.property(i).name === name) { return ep.property(i); }
        }
        return null;
    }

    function removeEffectByName(layer, name) {
        var ep;
        try { ep = layer.property("ADBE Effect Parade"); } catch (e) { return false; }
        if (!ep) { return false; }
        for (var i = ep.numProperties; i >= 1; i--) {
            if (ep.property(i).name === name) {
                ep.property(i).remove();
                return true;
            }
        }
        return false;
    }

